    private void limpiarTabla()
    {
        DefaultTableModel modelo = (DefaultTableModel) this.tablaChi.getModel();
        int aux = modelo.getRowCount();
        for ( int i = 0; i < aux ; i++)
        {
            modelo.removeRow(0);
        }
        
    }
