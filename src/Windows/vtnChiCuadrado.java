    private void agregarFilas(String intervalo, String observado, String esperado, String resta, String dividido)
    {
        DefaultTableModel model =  (DefaultTableModel) this.tablaChi.getModel();
        String datos[] = {intervalo, observado, esperado, resta, dividido};
        model.addRow(datos);
        
    }
