    private void btnGraficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraficarActionPerformed
        
        DefaultCategoryDataset dtsc = new DefaultCategoryDataset();
        
        for (int i = 0; i < this.tablaChi.getRowCount(); i++)
        {
            dtsc.addValue(Integer.parseInt((this.tablaChi.getValueAt(i, 1).toString())), "Observada", this.tablaChi.getValueAt(i, 0).toString());
            dtsc.addValue(Double.parseDouble((this.tablaChi.getValueAt(i, 2).toString())), "Esperada", this.tablaChi.getValueAt(i, 0).toString());
        }
        
        JFreeChart jf = ChartFactory.createBarChart3D("Grafica de frecuencias", "Intervalos", "Frecuencia", dtsc, PlotOrientation.VERTICAL, true, true, false);
        ChartPanel c = new ChartPanel(jf);
        JDialog vtn = new JDialog(new javax.swing.JFrame(), true);
        vtn.add(c);
        vtn.setSize(900, 700);
        vtn.setLocationRelativeTo(null);
        vtn.setVisible(true);
        
    }//GEN-LAST:event_btnGraficarActionPerformed

    private void btnTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTestActionPerformed
        
        
        if (cmbForma.getSelectedIndex() == 1)
        {
            tcc = new TestChiCuadrado( this.vtnDatos.getNumIntervalos(), this.vtnDatos.getNumAGenerar());
        }
        else 
        {
            //ValoresMetCongMult vtnDatos = new ValoresMetCongMult(new javax.swing.JFrame(), true);
            ValoresMetCongLineal vtnDatos = new ValoresMetCongLineal(new javax.swing.JFrame(), true);
            vtnDatos.setVisible(true);
            CongruencialLineal mcm = vtnDatos.datos();
            vtnDatosChiCuadrado vtnDatos2 = new vtnDatosChiCuadrado(new javax.swing.JFrame(), true);
            vtnDatos2.setVisible(true);
            for (int i = 0; i < vtnDatos2.getNumAGenerar(); i++)
            {
                mcm.generarNumero();
            }
            ArrayList<Double> datos = mcm.numerosGenerados();
            tcc = new TestChiCuadrado(vtnDatos2.getNumIntervalos(), datos);
        }
        ArrayList<String> intervalos = tcc.listaIntervalos();
        ArrayList<Double> resta = tcc.diferenciaYalCuadrado();
        ArrayList<Double> division = tcc.generarSumatoriaChi();
        double esperada = tcc.getEsperada();
        int observado[] = tcc.getIntervalos();
            
        for( int i = 0; i < intervalos.size() ; i++)
        {
            this.agregarFilas(intervalos.get(i), String.valueOf(observado[i]), String.valueOf(esperada), String.valueOf(resta.get(i)), String.valueOf(division.get(i)));
        }
        this.lblChiCalculado.setText(this.lblChiCalculado.getText() + tcc.generarChiCuadradoCalculado());
        this.lblChiTabulado.setText(this.lblChiTabulado.getText() + tcc.generarChiCuadradoTabulado());
        this.lblGrado.setText(this.lblGrado.getText() + String.valueOf(this.tcc.getGradosDeLibertad()));
        if (tcc.esAprobado()) 
        {
            this.lblAceptado.setVisible(true);
            this.lblExplicacion.setText("Los numeros pseudo-aleatorios generados satisfacen el test de prueba");
            this.lblExplicacion.setVisible(true);
        }
        else
        {
            this.lblCancelado.setVisible(true);
            this.lblExplicacion.setText("Los numeros pseudo-aleatorios generados NO satisfacen el test de prueba");
            this.lblExplicacion.setVisible(true);
        }
    }//GEN-LAST:event_btnTestActionPerformed

